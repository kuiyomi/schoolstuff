public class exercicio7L3for {
    public static void main (String args[]) {
        int num=100;

        for(int con=1;con<=50;con++) {
            if (num % 7 ==0){
                System.out.println("O " +con+ "º numero divisivel por 7 é " +num);
            }
        num++;
        }
    }    
}
